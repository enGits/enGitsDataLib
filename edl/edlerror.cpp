#include "edlerror.h"
#include <cstdlib>
#include <typeinfo>

namespace EDL_NAMESPACE
{

EdlError::EdlError(string message)
{
  m_Message = message;
  setName();
}

EdlError::~EdlError()
{
}

string EdlError::name()
{
  return m_Name;
}

void EdlError::setName()
{
  m_Name = typeid(*this).name();
}

} // namespace





